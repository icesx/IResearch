package cn.i.learn.scala.base

object Range {
    def ran() = {
        (1 to 10).map(x => x + 1)
    }
    def ran2() = {
        (1 to 10).foreach(x => x + 1)
    }
    def main(args: Array[String]): Unit = {
        println(Range.ran())
        println(Range.ran2())
        (1 to 103 by 5).foreach(println)
    }
}